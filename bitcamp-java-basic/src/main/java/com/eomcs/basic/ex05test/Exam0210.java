package com.eomcs.basic.ex05test;
//관계 연산자 < > 등위연산자 == !=
public class Exam0210 {
  public static void main(String[] args) {
    int a = 10;
    int b = 20;
    boolean r1 = a < b; //true
    System.out.println(a < b); //true
    System.out.println(a <= b);//true
    System.out.println(a > b);//false
    System.out.println(a >= b);//false
    System.out.println(a == b);//false
    System.out.println(a != b);//ture
  }
}
